load_env_or_prompt() {
  WAN_IP=$(curl -s ifconfig.me)

  declare -A ICONS=(
    ["IMAGE"]="üñºÔ∏è " ["NETWORK"]="ü™ê" ["WAN_IP"]="üåê"
    ["P2P_PORT"]="üîå" ["API_PORT"]="üß©" ["RPC_SEPOLIA"]="üõ∞Ô∏è "
    ["BEACON_SEPOLIA"]="üì°" ["PRIVATE_KEY"]="üîê" ["PROVER_ID"]="üíº"
    ["AGENT_COUNT"]="üë∑" ["DATA_DIR"]="üìÇ"
  )

  is_valid_input() {
    input="$1"
    echo "$input" | iconv -f UTF-8 -t ASCII//TRANSLIT &>/dev/null
  }

  prompt_input() {
    local key="$1" prompt="$2" default="$3" secret="$4" input=""
    while true; do
      if [[ "$secret" == "true" ]]; then
        read -s -p "$prompt" input
        printf "\n"
      else
        read -p "$prompt" input
      fi

      if [[ -z "$input" && -n "$default" ]]; then
        input="$default"
      fi

      input="$(echo -n "$input" | tr -d '\r\n')"

      if [[ -z "$input" && -z "$default" ]]; then
        echo "‚ö†Ô∏è Tr∆∞·ªùng n√†y b·∫Øt bu·ªôc ph·∫£i nh·∫≠p. Vui l√≤ng kh√¥ng ƒë·ªÉ tr·ªëng."
        continue
      fi

      if is_valid_input "$input" || [[ "$key" == "PRIVATE_KEY" || "$key" == "PROVER_ID" ]]; then
        echo "$input"
        return
      else
        echo "‚ùå L·ªói: gi√° tr·ªã ch·ª©a k√Ω t·ª± kh√¥ng h·ª£p l·ªá. Vui l√≤ng nh·∫≠p kh√¥ng d·∫•u."
      fi
    done
  }

  find_env_file() {
    local SEARCH_DIRS=(/ /mnt /opt)
    for base in "${SEARCH_DIRS[@]}"; do
      while IFS= read -r path; do
        if [[ -f "$path/.env" ]]; then
          ENV_FILE="$path/.env"
          DATA_DIR="$path"
          echo "üìÑ ƒê√£ ph√°t hi·ªán .env t·∫°i $ENV_FILE, ƒëang t·∫£i..."
          source "$ENV_FILE"
          export DATA_DIR
          return 0
        fi
      done < <(find "$base" -type d -name "aztec-prover" 2>/dev/null)
    done
    return 1
  }

  edit_env_variables() {
    echo ""
    echo "üîÑ .env hi·ªán t·∫°i:"
    for i in "${!env_lines[@]}"; do
      key="${env_lines[$i]%%=*}"
      val="${env_lines[$i]#*=}"
      [[ "$key" == "PRIVATE_KEY" ]] && val="********"
      printf "%2d. %-3s %-20s = %s\n" "$((i+1))" "${ICONS[$key]}" "$key" "$val"
    done

    echo ""
    if ! printf "‚úÖ C√≥\n‚ùå Kh√¥ng" | fzf --prompt="üîÅ B·∫°n c√≥ mu·ªën ch·ªânh s·ª≠a c√°c bi·∫øn m√¥i tr∆∞·ªùng? " --height=10 --reverse | grep -q "‚úÖ"; then
      echo "üîô Kh√¥ng ch·ªânh s·ª≠a bi·∫øn m√¥i tr∆∞·ªùng. Ti·∫øp t·ª•c..."
      return 0
    fi

    while true; do
      echo ""
      display_lines=()
      for line in "${env_lines[@]}"; do
        key="${line%%=*}"
        val="${line#*=}"
        [[ "$key" == "PRIVATE_KEY" ]] && val="********"
        display_lines+=("${ICONS[$key]} $key=$val")
      done

      selected=$(printf "%s\n" "${display_lines[@]}" "üíæ L∆∞u v√† ti·∫øp t·ª•c" | fzf --prompt="üîß Ch·ªçn bi·∫øn: " --height=40% --reverse)
      [[ $? -ne 0 || "$selected" == "üíæ L∆∞u v√† ti·∫øp t·ª•c" ]] && break

      key=$(echo "$selected" | awk -F '[ =]' '{print $2}')
      old_val=$(grep "^$key=" "$ENV_FILE" | cut -d= -f2-)
      if [[ "$key" == "PRIVATE_KEY" ]]; then
        new_val=$(prompt_input "$key" "üîê Nh·∫≠p gi√° tr·ªã m·ªõi cho $key: " "$old_val" true)
      else
        new_val=$(prompt_input "$key" "üîß Nh·∫≠p gi√° tr·ªã m·ªõi cho $key (hi·ªán t·∫°i: $old_val): " "$old_val")
      fi
      for i in "${!env_lines[@]}"; do
        [[ "${env_lines[$i]%%=*}" == "$key" ]] && env_lines[$i]="$key=$new_val"
      done
    done
  }

  backup_and_save_env() {
    echo ""
    if [ -f "$ENV_FILE" ]; then
      BACKUP_NAME="$ENV_FILE.bak_$(date +%Y%m%d_%H%M%S)"
      cp "$ENV_FILE" "$BACKUP_NAME"
      echo "üõ°Ô∏è ƒê√£ sao l∆∞u .env th√†nh: $BACKUP_NAME"
      ls -1t "$ENV_FILE".bak_* 2>/dev/null | tail -n +2 | xargs -r rm -f
    fi

    echo "üíæ ƒêang ghi t·ªáp .env..."
    {
      for line in "${env_lines[@]}"; do
        key="${line%%=*}"
        val="${line#*=}"
        val="$(echo -n "$val" | tr -d '\r\n')"
        echo "${key}=${val}"
      done
    } > "$ENV_FILE"

    dos2unix "$ENV_FILE" 2>/dev/null
    source "$ENV_FILE"
    export DATA_DIR
  }

  ### B·∫Øt ƒë·∫ßu x·ª≠ l√Ω ###
  if ! find_env_file; then
    DEFAULT_DATA_DIR="/root/aztec-prover"
    INPUT_DIR=$(prompt_input "DATA_DIR" "üìÇ Nh·∫≠p th∆∞ m·ª•c l∆∞u d·ªØ li·ªáu [m·∫∑c ƒë·ªãnh: $DEFAULT_DATA_DIR]: " "$DEFAULT_DATA_DIR")
    DATA_DIR="$INPUT_DIR"
    mkdir -p "$DATA_DIR"
    ENV_FILE="$DATA_DIR/.env"
  fi

  if [[ -f "$ENV_FILE" ]]; then
    env_lines=(
      "IMAGE=$IMAGE" "NETWORK=$NETWORK" "WAN_IP=$WAN_IP"
      "P2P_PORT=$P2P_PORT" "API_PORT=$API_PORT"
      "RPC_SEPOLIA=$RPC_SEPOLIA" "BEACON_SEPOLIA=$BEACON_SEPOLIA"
      "PRIVATE_KEY=$PRIVATE_KEY" "PROVER_ID=$PROVER_ID"
      "AGENT_COUNT=$AGENT_COUNT" "DATA_DIR=$DATA_DIR"
    )
    edit_env_variables || return 1
  else
    echo "üìÑ T·∫°o file .env m·ªõi..."
    IMAGE=$(prompt_input "IMAGE" "üñºÔ∏è  Nh·∫≠p Docker image [m·∫∑c ƒë·ªãnh: aztecprotocol/aztec:0.87.8]: " "aztecprotocol/aztec:0.87.8")
    NETWORK=$(prompt_input "NETWORK" "ü™ê Nh·∫≠p network [m·∫∑c ƒë·ªãnh: alpha-testnet]: " "alpha-testnet")
    RPC_SEPOLIA=$(prompt_input "RPC_SEPOLIA" "üõ∞Ô∏è  Nh·∫≠p Sepolia RPC URL: " "")
    BEACON_SEPOLIA=$(prompt_input "BEACON_SEPOLIA" "üì° Nh·∫≠p Beacon API URL: " "")
    PRIVATE_KEY=$(prompt_input "PRIVATE_KEY" "üîê Nh·∫≠p Publisher Private Key: "  true)
    PROVER_ID=$(prompt_input "PROVER_ID" "üíº Nh·∫≠p Prover ID: ")
    AGENT_COUNT=$(prompt_input "AGENT_COUNT" "üë∑ Nh·∫≠p s·ªë agent [m·∫∑c ƒë·ªãnh: 1]: " "1")
    P2P_PORT=$(prompt_input "P2P_PORT" "üîå Nh·∫≠p P2P Port [m·∫∑c ƒë·ªãnh: 40400]: " "40400")
    API_PORT=$(prompt_input "API_PORT" "üß© Nh·∫≠p API Port [m·∫∑c ƒë·ªãnh: 8080]: " "8080")

    env_lines=(
      "IMAGE=$IMAGE" "NETWORK=$NETWORK" "WAN_IP=$WAN_IP"
      "P2P_PORT=$P2P_PORT" "API_PORT=$API_PORT"
      "RPC_SEPOLIA=$RPC_SEPOLIA" "BEACON_SEPOLIA=$BEACON_SEPOLIA"
      "PRIVATE_KEY=$PRIVATE_KEY" "PROVER_ID=$PROVER_ID"
      "AGENT_COUNT=$AGENT_COUNT" "DATA_DIR=$DATA_DIR"
    )
  fi

  backup_and_save_env

  [[ ! -d "$DATA_DIR" ]] && echo "‚ö†Ô∏è Kh√¥ng t√¨m th·∫•y th∆∞ m·ª•c d·ªØ li·ªáu: $DATA_DIR" && return 1

  return 0  # ‚úÖ K·∫øt th√∫c h√†m v√† cho ph√©p ph·∫ßn sau ti·∫øp t·ª•c ch·∫°y
}

generate_compose() {
  mkdir -p "$DATA_DIR"  # üõ†Ô∏è ƒê·∫£m b·∫£o th∆∞ m·ª•c t·ªìn t·∫°i tr∆∞·ªõc
  COMPOSE_FILE="$DATA_DIR/docker-compose.yml"

  cat > "$COMPOSE_FILE" <<EOF
version: '3.8'
services:
  prover_node:
    image: $IMAGE
    container_name: prover_node
    restart: unless-stopped
    entrypoint: >
      sh -c 'node --no-warnings /usr/src/yarn-project/aztec/dest/bin/index.js start
      --prover-node --archiver --network $NETWORK'
    depends_on:
      broker:
        condition: service_started
    env_file:
      - .env
    environment:
      P2P_IP: "\${WAN_IP}"
      P2P_ANNOUNCE_ADDRESSES: "/ip4/\${WAN_IP}/tcp/\${P2P_PORT}"
      ETHEREUM_HOSTS: "\${RPC_SEPOLIA}"
      L1_CONSENSUS_HOST_URLS: "\${BEACON_SEPOLIA}"
      PROVER_PUBLISHER_PRIVATE_KEY: "\${PRIVATE_KEY}"
      PROVER_ENABLED: "true"
      P2P_ENABLED: "true"
      P2P_TCP_PORT: "\${P2P_PORT}"
      P2P_UDP_PORT: "\${P2P_PORT}"
      DATA_STORE_MAP_SIZE_KB: "134217728"
      LOG_LEVEL: "debug"
      PROVER_BROKER_HOST: "http://broker:\${API_PORT}"
    ports:
      - "\${API_PORT}:\${API_PORT}"
      - "\${P2P_PORT}:\${P2P_PORT}"
      - "\${P2P_PORT}:\${P2P_PORT}/udp"
    volumes:
      - \${DATA_DIR}/node:/data

  broker:
    image: $IMAGE
    container_name: broker
    restart: unless-stopped
    entrypoint: >
      sh -c 'node --no-warnings /usr/src/yarn-project/aztec/dest/bin/index.js start
      --prover-broker --network $NETWORK'
    env_file:
      - .env
    environment:
      DATA_DIRECTORY: /data
      ETHEREUM_HOSTS: "\${RPC_SEPOLIA}"
      LOG_LEVEL: "debug"
    volumes:
      - \${DATA_DIR}/broker:/data
EOF

  for i in $(seq 1 "$AGENT_COUNT"); do
    cat >> "$COMPOSE_FILE" <<EOF

  agent_$i:
    image: $IMAGE
    container_name: agent_$i
    restart: unless-stopped
    entrypoint: >
      sh -c 'node --no-warnings /usr/src/yarn-project/aztec/dest/bin/index.js start
      --prover-agent --network $NETWORK'
    env_file:
      - .env
    environment:
      PROVER_ID: "\${PROVER_ID}"
      PROVER_BROKER_HOST: "http://broker:\${API_PORT}"
      PROVER_AGENT_POLL_INTERVAL_MS: "10000"
    depends_on:
      - broker
EOF
  done
}

